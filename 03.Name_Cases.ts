//03• Name Cases: 

//• Store a person’s name in a variable.

let personName : String = "Mathan Kumar";

//• Print that person’s name in lowercase.
console.log("Lowercase:", personName.toLowerCase());

//• Print that person name in uppercase.
console.log("Uppercase:", personName.toUpperCase());

//• Print that person name in titlecase.
console.log("Titlecase:", personName[0].toUpperCase() + personName.slice(1).toLowerCase());





